use Auth;

class AuthController extends Controller
{
        protected $redirectPath = '/user/book';

    /**
     * Redirect the user to the Facebook authentication page.
     *
     * @return Response
     */
    public function redirectToProvider()
    {
        return Socialite::driver('facebook')->redirect();
    }
}
